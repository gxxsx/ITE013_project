		  <div class="panel-heading" style="background:#001a33;text-align:center;color:#fffff;">List Of Our Destinations</div>
		  <div class="panel-body" style="text-align:center;background:#fffff;">

		  <div>
			  <img src="img/matukad.jpg" style="height: 50%; width:70%;">
			  <h3>1. Matukad Island</h3>
			  <p>Matukad Island is one of the best islands in Caramon and it is the most popular one. The island have three powdery white sand beaches and crystal clear waters. One can also find an interesting lagoon, beautiful rock formations and tropical forest. The best time to visit the island is the morning so you will have the beach yourself, less people and no disturbances. It is also a great time to take pictures and dip into the waters.</p>
		</div>
		<div>
			  <img src="img/sabitanglaya.jpg" style="height: 50%; width:70%;">
			  <h3>2. Sabitang Laya Island</h3>
			  <p>Sabitang Laya Island is a triangular-shaped famous island in Caramoan. It has two (2) twin beaches, the Sabitang Laya Beach and the other is the Bag-ing Beach. Both of these two are composed of very long stretch (around 2 kilometers) of powdery white sand beach and pristine waters. Aside from that, the group of magnificent limestone rock formations in the island is one of its best features. This island is one of the best camping destinations in Caramoan. </p>
		</div>
		<div>
			  <img src="img/pitogo.jpg" style="height: 50%; width:70%;">
			  <h3>3. Pitogo Island</h3>
			  <p>Pitogo Island is known to have very calm waters and unique pebble-beach. While other islands and beaches in Caramoan have powdery white sand, in Pitogo Island, they are replaced by nature with very smooth small stones or pebbles. This is where most visitors in an island hopping tour relax, swim into the still waters and have their lunch. </p>
		</div>
		<div>
			  <img src="img/lahos.jpg" style="height: 50%; width:70%;">
			  <h3>4. Lahos Island</h3>
			  <p>The most interesting feature of Lahos Island is the white sand between two big limestone formations. So one can pass through the other side of the island easily, thus the meaning of “Lahos” to pass though. This island is just near Matukad Island, see the first on the list. </p>
		</div>
		<div>
			  <img src="img/cotivas.jpg" style="height: 50%; width:70%;">
			  <h3>5. Cotivas Island</h3>
			  <p>Cotivas Island is one of the paradise islands in Caramaon. It is composed of a long stretch fine sand beach and wide shallow waters. What makes it popular is that during low tide, one can find sandbars. During this time, one can walk into the shallow waters and go to the sandbars while enjoying the stunning view. The perfect swimming area is on the tip of the island. </p>
		</div>
		<div>
			  <img src="img/hunongan.jpg" style="height: 50%; width:70%;">
			  <h3>6. Hunongan Island</h3>
			  <p>Hunongan Island is an island covered with tropical forest and has a beautiful cove called Hunongan Cove. The beach is mantled with white sand and clear waters. Facing the Pacific Ocean, the waves in this cove are big but very consistent. The view of the big rock formations on both ends are amazing. This is a perfect destination for couples and stay at night in Hunongan Cove Resort. </p>
		</div>
		<div>
			  <img src="img/minalahos.jpg" style="height: 50%; width:70%;">
			  <h3>7. Minalahos Island</h3>
			  <p>Minalahos Island is a perfect swimming destination in Caramoan like the other beaches it also have very fine sugar-like white sand beach and clean sea water. It has tall limestone cliffs unlike the others. Some of the challenges in the famous TV Show “The Survivor” were taken here. </p>
		</div>
		<div>
			  <img src="img/gota.jpg" style="height: 50%; width:70%;">
			  <h3>8. Gota Island</h3>
			  <p>Gota Island is one of the island paradise in Caramaon where it has two coves with white sand beach with clear and cold waters perfect for swimming. The coves are open for public most of the time but it is closed sometimes. It is because there is a resort called Gota Village Resort in the island where sometimes the crew from the reality TV Show “Survivor” occupies the entire are for their scenes. It is a recommended resort for those who don’t mind staying in a luxury resort. </p>
		</div>
		<div>
			  <img src="img/tayak.jpg" style="height: 50%; width:70%;">
			  <h3>9. Tayak Beach</h3>
			  <p>Tayak Beach is a very inviting pure white sand beach located in between two huge limestone cliffs providing a great spot for swimming. One can even swim under some of the suspended sides of limestone formations. Behind the beach is the Tayak Lake with lush of green vegetation, a perfect place to pause for a moment, relax and meditate. Tayak Beach and Tayak Lagoon are located in Malarad Island. </p>
		</div>
		<div>
			  <img src="img/manlawi.jpg" style="height: 50%; width:70%;">
			  <h3>10. Manlawi Sandbar</h3>
			  <p>Of all the islands and beaches you will have to visit in Caramoan Islands, include Manlawi Island in your top destination. It is where one can find the extraordinary Manlawi Sandbar. During low tide, seems like all the waters are pulled off the shore creating a very vast expanse of powdery white sand that extends through the horizon. So you can run on the sand or play Frisbee since it would turn out like an Olympic-sized sandbar. And as the high tide approaches, the super wide sandbar will be covered with shallow waters painted with blue and green colors. </p>
		</div>

